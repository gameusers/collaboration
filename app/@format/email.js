/**
 * E-Mailアドレスを伏せ字にする
 * @param {string} value - E-Mailアドレス
 * @return {string} 伏せ字にされたE-Mailアドレス
 */
const formatEmailSecret = ({ value }) => {
  
  
  // --------------------------------------------------
  //   Return Value
  // --------------------------------------------------
  
  let returnValue = '';
  
  
  // --------------------------------------------------
  //   データ処理
  // --------------------------------------------------
  
  let emailLocalFlag = true;
  
  for (let i = 0; i < value.length; i++) {
    
    if (value[i] === '@') {
      emailLocalFlag = false;
    }
    
    if (i === 0 || emailLocalFlag === false) {
      returnValue += value[i];
    } else {
      returnValue += '*';
    }
    
  }
  
  
  // --------------------------------------------------
  //   Return
  // --------------------------------------------------
  
  return returnValue;
  
  
};




// --------------------------------------------------
//   Export
// --------------------------------------------------

module.exports = {
  formatEmailSecret,
};