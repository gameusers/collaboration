import React from 'react';
import { inject, observer } from 'mobx-react';

/** @jsx jsx */
import { css, jsx } from '@emotion/core';




// --------------------------------------------------
//   Class
// --------------------------------------------------

@inject('stores')
@observer
export default class extends React.Component {
  
  
  // --------------------------------------------------
  //   constructor
  // --------------------------------------------------
  
  constructor(props) {
    super(props);
  }
  
  
  // --------------------------------------------------
  //   render
  // --------------------------------------------------
  
  render() {
    
    
    // --------------------------------------------------
    //   Props
    // --------------------------------------------------
    
    const { stores } = this.props;
    
    
    // --------------------------------------------------
    //   Data - 必要な情報を取得
    // --------------------------------------------------
    
    const { loading, loadingLeft } = stores.layout;
    
    
    // --------------------------------------------------
    //   ローディング中でない場合、空のコンポーネントを返す
    // --------------------------------------------------
    
    if (!loading) {
      return null;
    }
    
    
    // --------------------------------------------------
    //   Return
    // --------------------------------------------------
    
    return (
      <div
        css={css`
          position: fixed;
          bottom: 20px;
          margin: 0;
          padding: 0;
        `}
        style={loadingLeft ? { left: '50px' } : { right: '50px' }}
      >
        
        <div className="la-pacman">
        	<div />
        	<div />
        	<div />
        	<div />
        	<div />
        	<div />
        </div>
        
      </div>
    );
    
  }
  
};